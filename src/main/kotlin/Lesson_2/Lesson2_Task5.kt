package org.example.Lesson_2
import kotlin.math.*

fun main() {
    val number1: Byte = 1
    val number2: Byte = 12

    val summa = 70000
    val stavka: Float = 16.7F
    val stavka100: Float = stavka / 100

    val year: Byte = 20
    val month: Int = year * 12

    val res1 = (summa * (number1 + stavka100 / number2 ).pow(n = month))

    println(String.format("%.4f",res1))
}